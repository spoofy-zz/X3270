// EbcdicCodec implementation
// ═══════════════════════════════════════════════════════════════════════════════
EbcdicCodec::EbcdicCodec(CodePage cp) : cp_(cp) {
    selectTables();
}

void EbcdicCodec::setCodePage(CodePage cp) {
    cp_ = cp;
    selectTables();
}

void EbcdicCodec::selectTables() {
    switch (cp_) {
    case CodePage::CP037:
        toUnicodeTable_ = kCP037_toUnicode;
        fromAsciiTable_ = kCP037_fromAscii;
        break;
    case CodePage::CP500:
        toUnicodeTable_ = kCP500_toUnicode;
        fromAsciiTable_ = kCP500_fromAscii;
        break;
    case CodePage::CP1047:
        toUnicodeTable_ = kCP1047_toUnicode;
        fromAsciiTable_ = kCP1047_fromAscii;
        break;
    }
}

uint16_t EbcdicCodec::toUnicode(uint8_t ebcdic) const {
    return toUnicodeTable_[ebcdic];
}

uint8_t EbcdicCodec::fromAscii(uint8_t ascii) const {
    if (ascii >= 128) return 0x3F; // '?' in EBCDIC
    return fromAsciiTable_[ascii];
}

// ── UTF-8 helpers ─────────────────────────────────────────────────────────────
static void appendUtf8(std::string& out, uint32_t cp) {
    if (cp < 0x80) {
        out += static_cast<char>(cp);
    } else if (cp < 0x800) {
        out += static_cast<char>(0xC0 | (cp >> 6));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    } else {
        out += static_cast<char>(0xE0 | (cp >> 12));
        out += static_cast<char>(0x80 | ((cp >> 6) & 0x3F));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    }
}

std::string EbcdicCodec::ebcdicToUtf8(const uint8_t* data, size_t len) const {
    std::string result;
    result.reserve(len);
    for (size_t i = 0; i < len; ++i) {
        uint32_t cp = toUnicodeTable_[data[i]];
        if (cp == 0 && data[i] != 0) cp = 0xFFFD; // replacement char
        appendUtf8(result, cp);
    }
    return result;
}

std::vector<uint8_t> EbcdicCodec::utf8ToEbcdic(const std::string& utf8) const {
    std::vector<uint8_t> result;
    result.reserve(utf8.size());
    // Simple ASCII-range conversion for Phase 1; non-ASCII → '?'
    for (unsigned char c : utf8) {
        if (c < 128) {
            result.push_back(fromAsciiTable_[c]);
        } else {
            // Skip continuation bytes; map leading bytes to '?'
            if ((c & 0xC0) != 0x80) {
                result.push_back(fromAsciiTable_['?']);
            }
        }
    }
    return result;
}

